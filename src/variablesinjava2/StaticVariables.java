package variablesinjava2;




public class StaticVariables {
static int a=10;
	
	
	
	public static void main(String[] args) {
		System.out.println(a);
		
		a = a+2;
		
		
		
		System.out.println(a);//calling of static variable in the same class
		
		
		
		a=60;
		
		System.out.println(a);//60
		
		
		
		
	
	
	}
	


}
