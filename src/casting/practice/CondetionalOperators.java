package practice;

public class CondetionalOperators {
public static void main(String[] args) {
	int i=10;
	int j=20;
	//equal to
	boolean n=i==j;
	System.out.println(n);
	//less than
	boolean l=i<=j;
	System.out.println(l);
	//greater than
	boolean s=i>j;
	System.out.println(s);
	
	
}
}
