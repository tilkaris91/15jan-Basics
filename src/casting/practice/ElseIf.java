package practice;

public class ElseIf {
public static void main(String[] args) {
	int i=65;
	int j=30;
	
	if(i<=40) {
		System.out.println("sanket");
	}
	else if(i>=50) {
		System.out.println("gaurav");
	}
	else if(i<=10) {
		System.out.println("rasika");
	}
	else if(i==65) {
		System.out.println("lahu");
	}
	else if(i==j) {
		System.out.println("abhijit");
	}
	else {
		System.out.println("vishakha");
	}
}
}
